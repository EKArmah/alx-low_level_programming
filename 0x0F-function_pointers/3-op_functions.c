#include "3-calc.h"

/**
 * op_add - adds two numbers together
 * @a: first number
 * @b: second number
 *
 * Return: result of operation
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers together
 * @a: first number
 * @b: second number
 *
 * Return: result of operation
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers together
 * @a: first number
 * @b: second number
 *
 * Return: result of operation
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers together
 * @a: first number
 * @b: second number
 *
 * Return: result of operation
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates modulus of two numbers together
 * @a: first number
 * @b: second number
 *
 * Return: result of operation
 */

int op_mod(int a, int b)
{
	return (a % b);
}
